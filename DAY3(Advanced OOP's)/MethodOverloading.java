/**
 * MethodOverloading
 */
public class MethodOverloading {

    

    public   void sum(int a, int b){
        int c= a+b;
        System.out.println("The sum of the given numbers is "+c);
    }

    public  void sum(int a, int b, int c){
        int d= a+b+c;
        System.out.println("The sum of the given number is "+d);
    }
}